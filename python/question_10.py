day_number = int(input("Enter a number (1-7): "))

if day_number == 1:
    print("Day is: Sunday")
elif day_number == 2:
    print("Day is: Monday")
elif day_number == 3:
    print("Day is: Tuesday")
elif day_number == 4:
    print("Day is: Wednesday")
elif day_number == 5:
    print("Day is: Thursday")
elif day_number == 6:
    print("Day is: Friday")
elif day_number == 7:
    print("Day is: Saturday")
else:
    print("Invalid input! Please enter a number between 1 and 7.")
